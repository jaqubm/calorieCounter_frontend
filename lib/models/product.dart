class Product {
  final String name;
  final int valuesPer;
  final int energy;
  final int protein;
  final int carbohydrates;
  final int fat;
  final String ownerEmail;

  Product({
    required this.name,
    required this.valuesPer,
    required this.energy,
    required this.protein,
    required this.carbohydrates,
    required this.fat,
    required this.ownerEmail,
  });
}